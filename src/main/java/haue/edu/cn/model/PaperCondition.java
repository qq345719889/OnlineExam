package haue.edu.cn.model;

public class PaperCondition {

	private Integer pid;
	
	private Integer perScore;
	
	private Integer num;
	
	private Integer qtype;
	
	private Integer questiondb;

	public Integer getQuestiondb() {
		return questiondb;
	}

	public void setQuestiondb(Integer questiondb) {
		this.questiondb = questiondb;
	}

	public Integer getPid() {
		return pid;
	}

	public void setPid(Integer pid) {
		this.pid = pid;
	}

	public Integer getPerScore() {
		return perScore;
	}

	public void setPerScore(Integer perScore) {
		this.perScore = perScore;
	}

	public Integer getNum() {
		return num;
	}

	public void setNum(Integer num) {
		this.num = num;
	}

	public Integer getQtype() {
		return qtype;
	}

	public void setQtype(Integer qtype) {
		this.qtype = qtype;
	}


	
}
